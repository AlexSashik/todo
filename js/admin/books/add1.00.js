$("#show_help").mouseover (function () {
    $("#help").css("display", "block");
});

$("#help").mouseover (function () {
    $("#help").css("display", "block");
});

$('#show_help').mouseout (function () {
    $("#help").css("display", "none");
});

$('#help').mouseout (function () {
    $("#help").css("display", "none");
});